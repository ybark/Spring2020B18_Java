package day17_While_Loops;

public class _09_WhileLoop_practice_II {

    public static void main(String[] args) {

        for (int i=0; i<100;i++) {
            System.out.print("*");
        }
        System.out.println();
        int i=0;
        while (i<100){
            System.out.print("*");
            i++;
        }


    }
}
